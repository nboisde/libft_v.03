#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*cs1;
	unsigned char	*cs2;

	i = 0;
	cs1 = (unsigned char *)s1;
	cs2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while (cs1[i] == cs2[i] && i < n - 1)
		i++;
	return ((int)((unsigned char)cs1[i] - (unsigned char)cs2[i]));
}
