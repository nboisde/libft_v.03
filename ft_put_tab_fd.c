#include "libft.h"

void	ft_put_tab_fd(char **tab, int fd)
{
	int	i;

	i = 0;
	while (tab[i])
	{
		ft_putstr_fd(tab[i++], fd);
		ft_putstr_fd("\n", fd);
	}
}
