#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*cdst;
	unsigned char	*csrc;

	cdst = (unsigned char *)dst;
	csrc = (unsigned char *)src;
	if (n == 0 || dst == src)
		return ((unsigned char *)dst);
	while (n--)
		*cdst++ = *csrc++;
	return ((unsigned char *)dst);
}
